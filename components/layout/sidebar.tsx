interface SidebarProps {
  profile: Profile
}

export function Sidebar({ profile }: SidebarProps) {
  const pathname = usePathname()
  const router = useRouter()
  const isAdmin = profile.role === 'admin'
  const navItems = isAdmin ? adminNav : portalNav

  async function handleSignOut() {
    const supabase = createClient()
    await supabase.auth.signOut()
    router.push('/login')
    router.refresh()
  }

  function isActive(item: NavItem) {
    if (item.exact) return pathname === item.href
    return pathname.startsWith(item.href)
  }

  return (
    <aside className="fixed left-0 top-0 h-screen w-64 bg-[#002147] flex flex-col z-30">
      {/* Brand */}
      <div className="px-6 py-5 border-b border-white/10">
        <div className="flex items-center gap-3">
          <Image
            src="/logo.png"
            alt="Xenith Capital"
            width={36}
            height={36}
            className="flex-shrink-0 object-contain"
          />
          <div>
            <span className="text-white font-bold text-sm leading-tight block tracking-wide">
              Xenith Capital
            </span>
            <span className="text-white/50 text-xs">
              Introducer Portal
            </span>
          </div>
        </div>
      </div>

      {/* Navigation */}
      <nav className="flex-1 px-3 py-4 overflow-y-auto">
        <ul className="space-y-0.5">
          {navItems.map((item) => {
            const active = isActive(item)
            return (
              <li key={item.href}>
                <Link
                  href={item.href}
                  className={cn(
                    'flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-all',
                    active
                      ? 'bg-[#5FB548]/20 text-white border-l-2 border-[#5FB548] pl-[10px]'
                      : 'text-white/70 hover:bg-white/10 hover:text-white'
                  )}
                >
                  <span className={active ? 'text-[#5FB548]' : ''}>{item.icon}</span>
                  {item.label}
                </Link>
              </li>
            )
          })}
        </ul>

        {/* Admin shortcut: invite introducer */}
        {isAdmin && (
          <div className="mt-6 px-3">
            <Link
              href="/admin/introducers/invite"
              className="flex items-center gap-2 w-full bg-[#5FB548] hover:bg-[#4ea038] text-white text-sm font-semibold px-3 py-2.5 rounded-lg transition"
            >
              <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 6v6m0 0v6m0-6h6m-6 0H6" />
              </svg>
              Invite Introducer
            </Link>
          </div>
        )}
      </nav>

      {/* User profile + sign out */}
      <div className="px-4 py-4 border-t border-white/10">
        <div className="flex items-center gap-3 mb-3">
          <div className="w-8 h-8 rounded-full bg-[#5FB548]/30 flex items-center justify-center flex-shrink-0">
            <span className="text-[#5FB548] font-semibold text-sm">
              {profile.full_name?.charAt(0)?.toUpperCase() ?? '?'}
            </span>
          </div>
          <div className="overflow-hidden">
            <p className="text-white text-sm font-medium truncate">{profile.full_name}</p>
            <p className="text-white/50 text-xs truncate">{profile.email}</p>
          </div>
        </div>
        <button
          onClick={handleSignOut}
          className="flex items-center gap-2 text-white/60 hover:text-white text-xs w-full px-1 py-1.5 rounded transition"
        >
          <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2}
              d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
          </svg>
          Sign out
        </button>
      </div>
    </aside>
  )
}
